package TypesOfMethods;

public class Circle1 {

	/*
	 * Calculate the area of circle using area as the method. value of radius has to
	 * be given as input to the method. Method which takes input value and returns
	 * output value
	 */

	double area(double newradius) {

		return 3.14 * newradius * newradius;

	}

}
