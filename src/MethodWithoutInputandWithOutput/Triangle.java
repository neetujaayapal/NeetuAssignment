package MethodWithoutInputandWithOutput;

public class Triangle {

	/*
	 * Write a program to print the area of a triangle with base and height as
	 * attributes and area as method.
	 */
	// Method which does not takes input value and returns an output value

	double base;
	double height;

	double area() {
		return 0.5 * base * height;
	}

}
