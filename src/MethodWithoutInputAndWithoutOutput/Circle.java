package MethodWithoutInputAndWithoutOutput;

public class Circle {

	/*
	 * Calculate the area of circle using area as the method and radius as the
	 * variable. Method which does not take input value and does not returns  output value
	 */

	int radius;

	void area() {

		System.out.println("Area = " + (3.14 * radius * radius));

	}

}
