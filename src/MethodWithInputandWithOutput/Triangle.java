package MethodWithInputandWithOutput;

public class Triangle {

	/*
	 * Write a program to print the area of a triangle with base 5 cm and area as
	 * method.The input to the method is height.
	 */
	// Method which takes input value and returns output value
	double base;

	double area(double height) {

		return 0.5 * base * height;
	}
}
